# Вариант 21 Задача 2: Дана строка, состоящая из русских слов, разделенных пробелами (одним или несколькими). Вывести строку, содержащую эти же слова, разделенные одним символом «.» (точка). В конце строки точку не ставить.

def replace_spaces_with_dots():
    text = input("Введите строку, состоящую из слов, разделенных пробелами: ")
    words = text.split() # Разбиваем строку на слова, убирая лишние пробелы
    result = ".".join(words) # Соединяем слова точкой
    print(result) # Выводим результат

replace_spaces_with_dots()
